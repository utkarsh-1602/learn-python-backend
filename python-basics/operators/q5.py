a = 10  # 1010 in binary
b = 4   # 0100 in binary

# Bitwise AND
print(a & b)    # Output: 0 (0000)

# Bitwise OR
print(a | b)    # Output: 14 (1110)

# Bitwise XOR
print(a ^ b)    # Output: 14 (1110)

# Bitwise NOT
print(~a)       # Output: -11 (inverting 1010 gives -1011)

# Bitwise Left Shift
print(a << 1)   # Output: 20 (10100)

# Bitwise Right Shift
print(a >> 1)   # Output: 5 (0101)
